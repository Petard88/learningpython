cars = ["bmw", "audi", "toyota", "subaru"]
print("Here is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars)) #sorted sorterar alfabetiskt temporärt

print("\nHere is the original list again:")
print(cars)

print(cars)

cars.reverse() #reverse byter håll på listan, dock inte alfabetiskt
print(cars)

print(len(cars))